currentIndex = 1

local frame=CreateFrame("Frame");-- Need a frame to capture events
frame:RegisterEvent("CHAT_MSG_GUILD");-- Register our event
frame:SetScript("OnEvent",function(self,event,msg,sender)-- OnEvent handler receives event triggers
    
    if msg == "开始游戏" or previousAnswerIsCorrect then
        startNewQuestion()
    elseif msg == "过" then
        SendChatMessage(quiz[currentIndex] .. " " .. answer[currentIndex] ,"GUILD" ,"language" ,"channel")
        startNewQuestion()
    elseif msg == "结束游戏" then
        finishGame()
    else   
        checkAnswer(event, sender, msg)
    end
end);

function startNewQuestion()
    previousAnswerIsCorrect = false
    currentIndex = math.random(amount)
    SendChatMessage(quiz[currentIndex] ,"GUILD" ,"language" ,"channel")
end

function finishGame()
    -- Print score
    for k, v in pairs(scoreTable) do
        scoreString = k .. "答对了 " .. v .. " 题！"
        SendChatMessage(scoreString ,"GUILD" ,"language" ,"channel")
    end
    scoreTable = {}
end

function checkAnswer(event, sender, msg)
    if msg == answer[currentIndex] then
        -- Correct answer
        previousAnswerIsCorrect = true
        -- Record score
        if scoreTable[sender] == nil then
            print("现在没有score，设置成1")
            scoreTable[sender] = 1
        else 
            print("现在有score是" .. scoreTable[sender] .. "，+1")
            scoreTable[sender] = scoreTable[sender] + 1
        end
        SendChatMessage(sender .. "回答正确！" ,"GUILD" ,"language" ,"channel")
    end
end